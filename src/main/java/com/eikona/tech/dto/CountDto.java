package com.eikona.tech.dto;

public class CountDto {
	
	private long totalInstalledDevice;
	
	private long totalOnline;
	
	private long totalOffline;
	
	private long totalPlant;
	
	private long totalBuilding;
	
	public long getTotalInstalledDevice() {
		return totalInstalledDevice;
	}
	public void setTotalInstalledDevice(long totalInstalledDevice) {
		this.totalInstalledDevice = totalInstalledDevice;
	}
	public long getTotalOnline() {
		return totalOnline;
	}
	public void setTotalOnline(long totalOnline) {
		this.totalOnline = totalOnline;
	}
	public long getTotalOffline() {
		return totalOffline;
	}
	public void setTotalOffline(long totalOffline) {
		this.totalOffline = totalOffline;
	}
	public long getTotalPlant() {
		return totalPlant;
	}
	public void setTotalPlant(long totalPlant) {
		this.totalPlant = totalPlant;
	}
	public long getTotalBuilding() {
		return totalBuilding;
	}
	public void setTotalBuilding(long totalBuilding) {
		this.totalBuilding = totalBuilding;
	}
	
	
	
}
